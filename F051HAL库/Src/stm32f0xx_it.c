/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
	
	//这里要设置成串口空闲中断，因为数据接收完了空闲了才进行中断
	if(__HAL_UART_GET_FLAG(&huart2,UART_FLAG_IDLE) == SET){
		//空闲中断来了以后我们就开始接收数据了
		//清空标志位
		__HAL_UART_CLEAR_IDLEFLAG(&huart2);
		//DMA停止,也就是处理DMA了
		HAL_UART_DMAStop(&huart2);
		//DMA总空间-DMA剩余的空间=接收到的数据长度
		len = USART2_DMA_REC_SIZE - hdma_usart2_rx.Instance->CNDTR;
		Usart2type.Usart2DMArecLen = len;
		//调用DMA接收的回调函数，来处理数据
		HAL_UART_RxCpltCallback(&huart2);
		}
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */
		//始能DMA接收
		HAL_UART_Receive_DMA(&huart2,Usart2type.Usart2DMARecBuff,\
		USART2_DMA_REC_SIZE);
  /* USER CODE END USART2_IRQn 1 */
}

